def GetParticleAlias(pdg):

  if pdg==+321:
    return "K^{+}"
  elif pdg==-321:
    return "K^{-}"

  elif pdg==+211:
    return "#pi^{+}"
  elif pdg==-211:
    return "#pi^{-}"

  elif pdg==+11:
    return "#e^{-}"
  elif pdg==-11:
    return "#e^{+}"

  elif pdg==+13:
    return "#mu^{-}"
  elif pdg==-13:
    return "#mu^{+}"

  elif pdg==22:
    return "#gamma"

  elif pdg==2212:
    return "p"

  elif pdg==2112:
    return "n"



